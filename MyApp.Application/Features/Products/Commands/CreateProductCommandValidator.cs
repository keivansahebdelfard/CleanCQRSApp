using FluentValidation;

namespace MyApp.Application.Features.Products.Commands
{
    public sealed class CreateProductCommandValidator
      : AbstractValidator<CreateProductCommand>
    {
        public CreateProductCommandValidator()
        {
            RuleFor(x => x.Name)
                .NotEmpty()
                .MaximumLength(200);

            RuleFor(x => x.Price)
                .GreaterThan(0);
        }
    }
}
